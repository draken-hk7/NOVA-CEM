"""Mission trajectory SVG export."""

from __future__ import annotations

from pathlib import Path
from typing import Any


def generate_trajectory_svg(result: Any, path: str | Path) -> Path:
    """Write altitude versus time for a mission result as a portable SVG graph."""

    points = list(getattr(result, "trajectory", []) or [])
    destination = Path(path)
    destination.parent.mkdir(parents=True, exist_ok=True)
    if not points:
        destination.write_text('<svg xmlns="http://www.w3.org/2000/svg" width="900" height="430"><text x="32" y="52">No trajectory data available.</text></svg>', encoding="utf-8")
        return destination

    time_values = [_value(point, "time_s") for point in points]
    altitude_values = [_value(point, "altitude_m") for point in points]
    max_time = max(max(time_values), 1.0)
    max_altitude = max(max(altitude_values), 1.0)
    width, height = 900.0, 430.0
    left, right, top, bottom = 80.0, 46.0, 56.0, 72.0
    plot_width = width - left - right
    plot_height = height - top - bottom
    polyline = " ".join(
        f"{left + plot_width * time / max_time:.2f},{height - bottom - plot_height * altitude / max_altitude:.2f}"
        for time, altitude in zip(time_values, altitude_values)
    )
    burnout_time = _value(result, "burn_time_s")
    burnout_index = min(range(len(points)), key=lambda index: abs(time_values[index] - burnout_time))
    burnout_x = left + plot_width * time_values[burnout_index] / max_time
    grid = []
    labels = []
    for index in range(6):
        ratio = index / 5.0
        x = left + ratio * plot_width
        y = height - bottom - ratio * plot_height
        grid.append(f'<path d="M {x:.2f} {height - bottom:.2f} V {height - bottom - plot_height:.2f}" stroke="#d6e0e5" stroke-width="1"/>')
        grid.append(f'<path d="M {left:.2f} {y:.2f} H {left + plot_width:.2f}" stroke="#d6e0e5" stroke-width="1"/>')
        labels.append(f'<text x="{x:.2f}" y="{height - bottom + 24:.2f}" text-anchor="middle">{ratio * max_time:.1f}</text>')
        labels.append(f'<text x="{left - 12:.2f}" y="{y + 4:.2f}" text-anchor="end">{ratio * max_altitude:.0f}</text>')
    peak_y = height - bottom - plot_height * _value(result, "max_altitude_m") / max_altitude
    destination.write_text(
        "\n".join(
            [
                '<svg xmlns="http://www.w3.org/2000/svg" width="900" height="430" viewBox="0 0 900 430" role="img" aria-label="NOVA mission altitude trajectory">',
                '<rect width="900" height="430" fill="#ffffff"/>',
                '<text x="36" y="38" font-family="Arial, Helvetica, sans-serif" font-size="22" font-weight="700" fill="#17202a">Mission Trajectory</text>',
                '<text x="36" y="61" font-family="Arial, Helvetica, sans-serif" font-size="12" fill="#5d6b74">Vertical screening trajectory with powered ascent and ballistic coast. Aerodynamic drag and guidance losses are not modeled.</text>',
                '<g font-family="Arial, Helvetica, sans-serif" font-size="11" fill="#4c5a64">',
                *grid,
                *labels,
                '</g>',
                f'<path d="M {left:.2f} {height - bottom:.2f} H {left + plot_width:.2f} M {left:.2f} {height - bottom:.2f} V {height - bottom - plot_height:.2f}" fill="none" stroke="#17202a" stroke-width="1.4"/>',
                f'<polyline points="{polyline}" fill="none" stroke="#1c7c68" stroke-width="3" stroke-linejoin="round" stroke-linecap="round"/>',
                f'<path d="M {burnout_x:.2f} {height - bottom:.2f} V {height - bottom - plot_height:.2f}" stroke="#c6623b" stroke-width="1.5" stroke-dasharray="5 4"/>',
                f'<text x="{burnout_x + 6:.2f}" y="{top + 22:.2f}" font-family="Arial, Helvetica, sans-serif" font-size="11" fill="#9b3f28">Burnout: {_value(result, "burnout_altitude_m"):.0f} m</text>',
                f'<circle cx="{left + plot_width:.2f}" cy="{peak_y:.2f}" r="4" fill="#9b3f28"/>',
                f'<text x="{left + plot_width - 8:.2f}" y="{peak_y - 10:.2f}" text-anchor="end" font-family="Arial, Helvetica, sans-serif" font-size="11" fill="#9b3f28">Apogee: {_value(result, "max_altitude_m"):.0f} m</text>',
                f'<text x="{left + plot_width - 8:.2f}" y="{height - 22:.2f}" text-anchor="end" font-family="Arial, Helvetica, sans-serif" font-size="11" fill="#4c5a64">Coast gain: {_value(result, "coast_altitude_m"):.0f} m</text>',
                f'<text x="{left + plot_width / 2:.2f}" y="{height - 20:.2f}" text-anchor="middle" font-family="Arial, Helvetica, sans-serif" font-size="12" fill="#17202a">Time (s)</text>',
                f'<text x="20" y="{height / 2:.2f}" transform="rotate(-90 20 {height / 2:.2f})" text-anchor="middle" font-family="Arial, Helvetica, sans-serif" font-size="12" fill="#17202a">Altitude (m)</text>',
                '<metadata>Generated by NOVA mission trajectory screening model.</metadata>',
                '</svg>',
            ]
        ),
        encoding="utf-8",
    )
    return destination


def _value(value: Any, attribute: str) -> float:
    item = getattr(value, attribute, None)
    if item is None and isinstance(value, dict):
        item = value.get(attribute)
    try:
        return float(item)
    except (TypeError, ValueError):
        return 0.0
